package br.com.seta.processo.exceptions;

public class ExecuteRestApiException extends Exception {
	
	private static final long serialVersionUID = 1L;

	public ExecuteRestApiException() {
		super();
	}

	public ExecuteRestApiException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ExecuteRestApiException(String arg0) {
		super(arg0);
	}

	public ExecuteRestApiException(Throwable arg0) {
		super(arg0);
	}
	
	

}
