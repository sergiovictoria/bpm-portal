package br.com.seta.processo.exceptions;

public class SuprimentoException extends Exception {

	private static final long serialVersionUID = 1L;
	
	
	
	public SuprimentoException() {
	}

	
	public SuprimentoException(String arg0) {
		super(arg0);
	}

	public SuprimentoException(Throwable arg0) {
		super(arg0);
	}
	

	public SuprimentoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
	
	

}
