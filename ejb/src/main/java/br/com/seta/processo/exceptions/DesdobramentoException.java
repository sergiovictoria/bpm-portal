package br.com.seta.processo.exceptions;

public class DesdobramentoException extends Exception {

	private static final long serialVersionUID = 1L;
	
	
	
	public DesdobramentoException() {
	}

	
	public DesdobramentoException(String arg0) {
		super(arg0);
	}

	public DesdobramentoException(Throwable arg0) {
		super(arg0);
	}
	

	public DesdobramentoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
	
	

}
