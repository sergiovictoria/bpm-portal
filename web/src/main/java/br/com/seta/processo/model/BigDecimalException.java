package br.com.seta.processo.model;

public class BigDecimalException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	
	
	public BigDecimalException() {
	}

	
	public BigDecimalException(String arg0) {
		super(arg0);
	}

	
	public BigDecimalException(Throwable arg0) {
		super(arg0);
	}
	
	
	public BigDecimalException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
	

}
